export function getCulturesForContinents(selectedContinents: string[]): CultureOption[] {
  const cultures: CultureOption[] = [];
  for (const continent of selectedContinents) {
    if (culturesByContinent[continent]) {
      cultures.push(...culturesByContinent[continent]);
    }
  }
  return cultures;
}
